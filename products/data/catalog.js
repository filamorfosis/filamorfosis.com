let catLang = localStorage.getItem('preferredLanguage') || 'es';

function t(key) {
    return (CAT_TRANSLATIONS[catLang] && CAT_TRANSLATIONS[catLang][key])
        || CAT_TRANSLATIONS['es'][key] || key;
}

function applyCatTranslations() {
    document.querySelectorAll('[data-t]').forEach(el => {
        const key = el.getAttribute('data-t');
        el.textContent = t(key);
    });
    const searchEl = document.getElementById('catSearch');
    if (searchEl) searchEl.placeholder = t('search_placeholder');
    // Update category labels in CATEGORIES array to match current lang
    CATEGORIES[0].label = t('cat_uv');
    CATEGORIES[1].label = t('cat_3d');
    CATEGORIES[2].label = t('cat_laser');
    CATEGORIES[3].label = t('cat_photo');
}

function setCatLang(lang) {
    catLang = lang;
    localStorage.setItem('preferredLanguage', lang);

    // Sync desktop <select>
    var sel = document.getElementById('navLangSelect');
    if (sel) sel.value = lang;

    // Update active states on all lang option types
    document.querySelectorAll('.lang-option, .lang-option-mobile').forEach(function(o) {
        o.classList.toggle('active', o.dataset.lang === lang);
    });

    applyCatTranslations();
    if (typeof renderAll === 'function') renderAll();
}

