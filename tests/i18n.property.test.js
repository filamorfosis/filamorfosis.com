const LANGUAGES = Object.keys(STORE_I18N); // ['ES', 'EN', 'DE', 'PT', 'JA', 'ZH']
const ALL_KEYS = Object.keys(STORE_I18N.ES); // ES is the canonical key set

// ---------------------------------------------------------------------------
// Verify all languages have exactly the same keys as ES
// ---------------------------------------------------------------------------
for (const lang of LANGUAGES) {
  const missingKeys = ALL_KEYS.filter(k => !(k in STORE_I18N[lang]));
  if (missingKeys.length > 0) {
    console.error(`[SETUP ERROR] Language "${lang}" is missing keys: ${missingKeys.join(', ')}`);
    process.exit(1);
  }
}

// ---------------------------------------------------------------------------
// Property 28: Translation map completeness
// Validates: Requirements 13.1, 13.2, 13.4
// ---------------------------------------------------------------------------
let p28Passed = false;
try {
  fc.assert(
    fc.property(
      fc.constantFrom(...LANGUAGES),
      fc.constantFrom(...ALL_KEYS),
      (lang, key) => {
        const val = STORE_I18N[lang][key];
        return typeof val === 'string' && val.length > 0;
      }
    ),
    { numRuns: 200 }
  );
  p28Passed = true;
  console.log('✅ Property 28 PASSED: Translation map completeness — all keys return non-empty strings for all 6 languages');
} catch (err) {
  console.error('❌ Property 28 FAILED: Translation map completeness');
  console.error(err.message);
}

